package com.fortycoderplus.sample.calcite;

import org.apache.calcite.sql.SqlNode;
import org.apache.calcite.sql.dialect.PostgresqlSqlDialect;
import org.apache.calcite.sql.parser.SqlParseException;
import org.apache.calcite.sql.parser.SqlParser;
import org.apache.calcite.sql.util.SqlString;

public class SqlParse {

    public static void main(String[] args) throws SqlParseException {
        SqlParser parser = SqlParser.create(
                "SELECT * FROM t1 ORDER BY ID LIMIT 10", SqlParser.Config.DEFAULT);
        SqlNode sqlNode = parser.parseStmt();
        SqlString sqlString = sqlNode.toSqlString(PostgresqlSqlDialect.DEFAULT);
        System.out.println(sqlString.getSql());
    }
}
